// adds E around the data, it helps with applying the rules around the edges
function addFloorAround(gameOfLifeArray) {
  const gameOfLife = [...gameOfLifeArray];
  // add E (stands for end) around the array
  gameOfLife.forEach((ele, i) => {
    gameOfLife[i] = 'E' + ele + 'E';
  });
  let outerLimit = '';
  for (let i = 0; i < gameOfLife[0].length; i++) {
    outerLimit += 'E';
  }
  // add it above
  gameOfLife.push(outerLimit);
  // add it below
  gameOfLife.unshift(outerLimit);
  return gameOfLife;
}

// transform the string array into a 2d array with all the variabls
function createInitalSeatingPlan(gameOfLifeArray) {
  const initialSeatingPlan = new Array(gameOfLifeArray.length);
  for (let row = 0; row < gameOfLifeArray.length; row++) {
    initialSeatingPlan[row] = new Array(gameOfLifeArray[row].length);
    for (let column = 0; column < gameOfLifeArray[row].length; column++) {
      const seatOrFloor = gameOfLifeArray[row][column];
      initialSeatingPlan[row][column] = seatOrFloor;
    }
  }
  return initialSeatingPlan;
}

